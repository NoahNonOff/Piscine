int	ft_atoi(char *str)
{
	int	i;
	int	res;
	int	negat;

	i = 0;
	res = 0;
	negat = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negat *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res += str[i++] - '0';
	}
	if (negat < 0)
		return (-res);
	return (res);
}
